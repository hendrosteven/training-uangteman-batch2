package com.uangteman.dto;

public class FormTanggal {
	private String awal;
	private String akhir;
	
	public FormTanggal(){}

	public String getAwal() {
		return awal;
	}

	public void setAwal(String awal) {
		this.awal = awal;
	}

	public String getAkhir() {
		return akhir;
	}

	public void setAkhir(String akhir) {
		this.akhir = akhir;
	}
	
	
}
